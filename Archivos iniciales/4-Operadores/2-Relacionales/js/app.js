"use strict"

//********************************
//*** Operadores Relacionales

var datoA = 10;
var datoB = 20;

// MAYOR QUE
var mayorQue = datoA > datoB;
console.log(datoA+' > '+datoB+': '+mayorQue);

// MENOR QUE
var menorQue = datoA < datoB;
console.log(datoA+' < '+datoB+': '+menorQue);

// MAYOR O IGUAL QUE
var mayorIgualQue = datoA >= datoB;
console.log(datoA+' >= '+datoB+': '+mayorIgualQue);

//MENOR O IGUAL QUE
var menorIgualQue = datoA <= datoB;
console.log(datoA+' <= '+datoB+': '+menorIgualQue);

// IGUAL QUE
var igualQue = datoA == datoB;
console.log(datoA+' == '+datoB+': '+igualQue);

// ES DIFERENTE QUE
var diferenteQue = datoA != datoB;
console.log(datoA+' != '+datoB+': '+diferenteQue);