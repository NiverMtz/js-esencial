"use strict"

//********************************
//*** Operadores Lógicos

var datoA = 10;

// ASIGNACIÓN SIMPLE
var igual = datoA;
console.log('Asignación simple: '+ igual);

// SUMAR Y ASIGNAR
var masIgual = 10;
masIgual += datoA;
console.log('Sumar y asignar '+ masIgual);

// RESTAR Y ASIGNAR
var menosIgual = 10;
menosIgual -= datoA;
console.log('Restar y asignar: '+ menosIgual);

// MULTIPLICAR Y ASIGNAR
var porIgual = 10;
porIgual *= datoA;
console.log('Producto y asignar: '+ porIgual);

// DIVIDIR Y ASIGNAR
var entreIgual = 20;
entreIgual /= datoA;
console.log('Dividir y asignar: '+ entreIgual);